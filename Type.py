"""
This code explains truncating and rounding a number,
with a decimal point
"""
#Importing math library
from math import trunc
x = 5.5153
Truncated_x = trunc(x)
rounded_x = round(x)

print('This is the truncated and rounded result of x: ')
print(f"This is the truncated result: {Truncated_x}, this is the rounded result {rounded_x}")